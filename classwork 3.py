# taking values from the user
x = input('Enter the value of x')
y = input('Enter the value of y')
z = input('Enter the Value of z')
print ('The Initial values are')
print(' x is ',x)
print(' y is ',y)
print(' z is ',z)
# swapping the values
temp = x
x = y
y = z
z = temp
#printing swapped values
print( 'After Swapping the final values are')
print(' x is', x)
print(' y is', y)
print(' z  is', z)
